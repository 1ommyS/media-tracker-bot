package com.indistudia.bot.statemachine;

import com.indistudia.bot.CommandContext;

import java.util.Optional;

/**
 * Контракт конечного автомата (FSM) для диалоговых сценариев Telegram-бота.
 *
 * <p>Назначение:
 * хранить и обрабатывать многошаговые пользовательские сценарии, где одной команды недостаточно.
 * Пример: интерактивное добавление фильма в личный список с последовательным сбором данных.
 *
 * <p>Функциональные требования (целевая модель, пока без реализации):
 * <ul>
 *   <li>FR-1: у пользователя может быть только один активный сценарий в каждый момент времени.</li>
 *   <li>FR-2: состояние сценария должно переживать рестарт процесса (через БД/кеш) после внедрения реализации.</li>
 *   <li>FR-3: входящие сообщения сначала должны попадать в FSM, и только затем в командный роутер.</li>
 *   <li>FR-4: FSM должна уметь отменять сценарий по универсальной команде (`/cancel`).</li>
 *   <li>FR-5: FSM должна возвращать понятный ответ пользователю на каждом шаге.</li>
 *   <li>FR-6: ошибки в шагах не должны ломать сессию целиком, кроме критических ошибок целостности.</li>
 * </ul>
 *
 * <p>Текущий статус:
 * интерфейс используется в реальной команде-примере {@code /wizard start}.
 * Дополнительные сценарии можно добавлять по новым {@code flowKey} без изменения контракта.
 */
public interface BotStateMachine {
    /**
     * Запускает сценарий для пользователя.
     *
     * @param context контекст команды (текущий пользователь)
     * @param flowKey ключ сценария, например {@code "film-tracking"}
     * @return результат запуска сценария
     */
    StateMachineStartResult startFlow(CommandContext context, String flowKey);

    /**
     * Проверяет, есть ли активный сценарий у пользователя.
     *
     * @param context контекст команды
     * @return {@code true}, если пользователь находится внутри шага FSM
     */
    boolean hasActiveFlow(CommandContext context);

    /**
     * Пробует обработать входящее сообщение как шаг активного сценария.
     *
     * @param context контекст команды
     * @param incomingText входящий текст сообщения
     * @return ответ пользователю, если сообщение обработано FSM; пустое значение, если FSM не обработала сообщение
     */
    Optional<String> tryHandle(CommandContext context, String incomingText);
}
